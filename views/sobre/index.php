            <h1>Um pouco da nossa história</h1>
            <div class="entry">
                <blockquote>
                    <div class="inner">
                        <p>Já são 35 anos de Qualidade, Responsabilidade, Profissionalismo e Paixão em servir nossos clientes através de Diagnósticos Laboratoriais, com os mais altos padrões de qualidade.</p>
                    </div>
                </blockquote>

                <p>Fundado em 1973 pelos Senhores José Barbosa Lima, Moacir José Silva e o farmacêutico Abrão Name, na época o Laboratório Moderno funcionava na Galeria Póvoa situada na Rua 07 (Rua do Lazer), Centro. Suas instalações foram montadas em três salas, sendo uma para recepção e coleta e outras duas para área técnica. Contava com três funcionários, além dos sócios fundadores. Em 1976, foi transferido ao Farmacêutico/Bioquímico Dr. Ulisses Tuma que juntamente com sua esposa, a também Farmacêutica/ Bioquímica Dra. Ilenir Leão Tuma, implementaram um novo conceito de trabalho ao laboratório iniciando uma importante trajetória de crescimento. Nesta época sua sede foi transferida para o setor Campinas, mais precisamente para a Rua Pouso Alto e em seguida para a a sua Sede Própria na Avenida Minas Gerais nº 710.</p>
                <p>Atualmente são cinco unidades em regiões estratégicas de Goiânia e cidades vizinhas.</p>
                <p><span class="quote_right">Durante sua trajetória de crescimento, o Laboratório sempre se preocupou com a qualidade e confiabilidade de seus serviços</span>Durante sua trajetória de crescimento, o Laboratório sempre se preocupou com a qualidade e confiabilidade de seus serviços bem como os desenvolvimentos tecnológicos e científicos da área, automatizando seus exames e processos implementando seu Sistema de Gestão da Qualidade e investindo em seu quadro de colaboradores que conta hoje com especialistas das mais diversas áreas das análises clínicas.</p>
                <p>O Laboratório Moderno também passou a oferecer aos laboratórios da região uma nova opção em serviços de apoio e está preparado para atender as exigências da Anvisa ( Agência Nacional de Vigilância Sanitária).</p>
                <p>Em 2011, o Laboratório novamente trocou de proprietários, foi adquirido pelo Médico Dr. Franks Augusto Valim e sua esposa Biomédica e Administradora Hospitalar Drª Maria Fernanda de Araujo Tomaz Valim que neste momento vem implementando uma nova fase para que o Laboratório  continue honrando o nome MODERNO, renovando seus equipamentos e suas instalações, sempre preocupados com a qualidade e confiabilidade dos exames por nós realizados.</p>
                <p>Essa é a nossa história, 35 anos de Qualidade, Responsabilidade, Profissionalismo e Paixão pela nossa empresa e por Diagnóstico Laboratorial!</p>
