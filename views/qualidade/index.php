                        <p>A Garantia da Qualidade engloba as atividades relacionadas com os processos pré-analíticos, analíticos e pós-analíticos.
                            Nossa qualidade se refere à satisfação do paciente; os médicos que solicitam os exames; as empresas prestadoras de serviços de saúde e a nossa equipe como um todo.
                            O nosso controle de qualidade nos possibilita avaliar a precisão e exatidão dos métodos analíticos.
                            Junto com o  controle de qualidade temos a Garantia da Qualidade com o objetivo de promover confiança na avaliação e garantir que cada setor de trabalho possa obter produtos (resultados de exames) de boa qualidade.
                        </p>
                        <p>Com a padronização de  nossos serviços alcançamos a qualidade desejada desde o atendimento até a liberação do laudo.</p>
                        <p>Com a incessante procura por qualidade em nossos serviços, conseguimos ACREDITAR  nosso laboratório. Esta Acreditação da Qualidade é realizada pela Sociedade Brasileira de Análises Clínicas (SBAC), e só a consegue os laboratórios que comprovam a ótima qualidade nos resultados de seus exames, e a administração dos controles externos e internos, e a satisfação de pacientes, funcionários e médicos solicitantes.
                            Conseguimos a acreditação (DICQ) desde 2005.
                        </p>
